import config from "../../config";

export const browserTesterScriptTemplate = `
<script id="__bt_script__">//<![CDATA[
  (function() {
    try {
      var script = document.createElement('script');
      if ('async') {
        script.async = true;
      }
      script.src = 'http://HOST:${config.proxy.port}/browser-tester-static/index.js?t=TIME'.replace("HOST", location.hostname).replace("TIME", Date.now());
      if (document.body) {
        document.body.appendChild(script);
      } else if (document.head) {
        document.head.appendChild(script);
      }
    } catch (e) {
      console.error("Browsertester: could not append script tag", e);
    }
  })()
//]]></script>`;

